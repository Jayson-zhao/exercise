            <h1>《妖精的旋律》：日式YY的经典案例</h1>
            <p>可以肯定，有一部分日本动漫是很“少儿不宜”的。《妖精的旋律》就是其中之一。血腥就不必说了。与一般动画回避暴力不同，这个系列直面、甚至是凸显血腥场面。最让某些人士受不了的，就是那些直观的肢解场景了。血腥之外，是裸体。其实这也没啥，因为这毕竟不是Hentai，所以都是很简略的线条，应该不会引起任何生理反应——除非看的哥们有些过于饥渴。可是，裸体对象一般是幼齿，这就不大好玩。像《魔女之刃》中好歹还是个熟女，比较有看头。幼齿就留给变态者们吧，俺们纯洁人士就爱看……“三个代表”。</p>
            <p>当然，血腥和裸体都不算大罪恶，大罪恶还是在于对象是幼齿。也就是说，这个动画系列，对于摧残幼齿是不遗余力的。有两位小幼齿被整得特别惨不忍睹，一个是曾经变成“人干”的娜娜，木乃伊的造型实在让人受不了；一个是浩太的妹妹，在主角——其实就相当于代入感情的观众面前——直接被肢解，这个未免也太具有冲击力了点。</p>
            <p>首先，露西同学的双重人格，就是让日本男人发狂的女性性格代表。一种是透着神秘冷酷劲的强力杀手，负责带给日本男人SM的快感；一个是白痴般痴情的人形宠物，满足日本男人狂妄自大的心理。要么被皮鞭抽得满地打滚，还得穿兜裆布；要么抱个小萝莉在膝盖上，人家还任你摆布还叫你“爸爸”……不要跟我说这是所有男人的理想，俺就不这么想。俺还是倾向于平等而妩媚型的，棋逢对手就好了，被抽鞭子和玩宠物都不是俺的嗜好，但日本男人潜意识里似乎挺好那口的。</p>
            <p>其次，片中萝莉们为了男人要死要活的样子，更让人受不了。先说爱情这方面，无论是堂妹，还是露西，还是喵喵，还有那个寄居的小萝莉（啥名字忘了），都对男主角粘得不行，“浩太浩太”地叫得人浑身起鳄鱼皮疙瘩。而浩太这鸟人，就是典型的日本男人中的正面形象，在无数青春偶像艳情文艺片里都见识过了的。只能说日本女生都太好骗了，或者动画超级抽空了现实合理性，否则这样的小男生能吸引到多少女性还真难说。当然，您可以说俺是在嫉妒；对于这点，我完全没法否认。</p>
            <p>爱情方面的女追男，相对而言，还可以接受些。毕竟男猪脚关键时候还是有些勇气的，平常也算怜香惜玉。然而，某些亲情的描写，就让人发狂暴走了。显然，大家都猜到了，俺说的是娜娜和她“爸爸”的感情。正如同接受毛片中“与爬行类动物同眠”一样，俺也不太能接受动画片中宣扬的忠实到变态的感情。娜娜都变成人干了，还在那汪着一眶泪水说：“对不起，弄脏了爸爸的衣服”。这就不是亲情，甚至不是矫情，而是天杀的滥煽情了。从戏剧手法来说，你可以说它将某种感情渲染到极端，或者说用极端的手法来渲染，怎么说都行。但就是令人很不舒服。其他动画中也没少玩这个，例如《枪姬》（Gunslinger Girl）系列，也是大致相同的设定，只不过愚忠的萝莉们拿的是现实中的武器。</p>

            <h1>《东方承诺》：不动如山，暗流涌动</h1>
            <p>《东方承诺》与导演的以往作品不同之处在于将温情大力融入作品之中，这无疑与导演年轻时期的《苍蝇》有着某种暗合，而与耳熟能详的《撞车》、《暴力史》的种种异化有所不同。按照本片小品式的格局以及刻意收敛的种种叙事细节，《东方承诺》更像是一种模式化的暴力仪式，浓缩了导演对于暴力概念性总结。</p>
            <p>影片让人很清晰的感受到在伦敦的俄罗斯黑帮的内幕和规则，这与经典《教父》有些相似。故事情节与人物塑造多少让人想起《无间道》的卧底概念，而其他种种也是疑似为俗套剧情安排。而且，影片的很多理念借鉴于东方文化，如加入黑帮的仪式、如环环相扣的命运报应，以及片名中使用承诺这个词汇。</p>
            <p>其实，《东方承诺》更像是导演对应主流电影的一种表达。然而，这样的问题并不重要，重要的是看柯南博格影像上的形式化美感，这也是柯南博格电影的强势所在。首先，柯南博格刻意的将种种戏剧性因素统统淡化，观众仅仅依赖纯熟的暗示跟进故事情节。重要的是，柯南博格营造的暴力世界的味道，是那种形式化的影像处理。</p>
            <p>《东方承诺》的影像一直很平静，漫不经心一般。但凝练出来的味道却是让人心仪和心悸的压抑，这暗合了人们对于暴力世界的日常心理，好奇又恐惧。柯南博格作品的独特魅力就是对于暴力行为的影像展示。影片并未以打杀为目的，仅有的两段割喉段落就直截了当的显示了萧杀之气，是暴力行为的直接观感。这种还原于真实也艺术化的处理实质是对于生理现象的客观展示。</p>
            <p>所以，柯南博格式的暴力的形式美分外有力。观众可以一面感受暴力行为的残酷恐怖，一面也有对暴力行为的崇拜和生理依赖。这就是导演的理念，展示真实世界中客观的无法否定的冷冰冰一面。影片对于暴力概念的顶礼膜拜在于著名的“浴室裸杀”。这个段落中，画面的亮度足够展示男性身体的力量美，雨果维森特的完美身材足够延展到古希腊的男性美范畴。</p>

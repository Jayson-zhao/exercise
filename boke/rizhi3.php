            <h1>《反恐王国》：生猛的空心弹</h1>
            <p>当然，也不用那么兴奋，因为以规模而论，本片的枪战场面是无法与《黑鹰》比拟的，毕竟后者是场小规模的战争，出动的是游骑兵和三角洲部队，《反恐王国》（The Kingdom）是小规模的精英反恐，正如同各位哥哥弟弟在CS里打联网那种。</p>
            <p>说到精英反恐，恐怕等到同志们看到此片时，又会重提“大美国主义”的话题。因为在本片中，联邦调查局的干探们被塑造得比较牛A，虽然也受沙特本地势力的阻扰，但基本上算是马到成功，在一周不到的时间里，就解决了沙特军警可能永远也解决不了的恐怖案件。</p>
            <p>除开政治因素，沙特军警在事故调查方面的无知与幼稚让人汗颜。影片虽然没有直接指出这一点，但通过对于FBI干探们“精明”的描写，已经反向说明了这点。沙特是啥国家？中东最发达的地区，也可能是这个世界上最富裕的国家，而且一早就被西化了——至少在教育和高层管理上是如此。我很难想象，他们在调查常识这一点上，比普通人的经验还不如。</p>
            <p>当然，狡猾的好莱坞也不好意思过分夸大FBI们的神勇，毕竟自大的美国佬形象不好，这点是全球都知道的，好莱坞心底里也跟明镜儿似的。所以，片中的FBI们没说啥上纲上线的废话，整个是按“工作狂”的标准来塑造的。也就是说，美国小探们的专业素养是影片描绘的重心，而不是什么不着调的“自由世界”、“人权”之类的。</p>
            <p>沙特军警既然调查技术的不行，那就只好提供人力支持了。这倒成为了影片的一个亮点，因为扮演“友情支援”角色的阿拉伯演员，个人觉得演技很朴实真挚，特有亲切感。属于那种着墨不多，但在剧本设定阶段比较打眼的角色。相对而言，FBI一帮人都有些相形逊色了，即使是奥斯卡影帝杰米福克斯（Jamie Foxx）也被阿拉伯络腮胡子男抢去了风头，更不用提本来就是“火鸡”类型的詹妮弗嘎纳（Jennifer Garner）了。库柏（Christ Cooper）的角色稍微好一点，老戏骨就是不同，懂得在每一秒迸出火花。不过，要说大有作为，也是不太可能的。</p>

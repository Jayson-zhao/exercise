            <h1>军训感言</h1>
            <p>山没有悬崖峭壁就不再险峻，海没有惊涛骇浪就不再壮阔，河没有跌宕起伏就不再壮美，人生没有挫折磨难就不再坚强。樱花如果没有百花争艳我先开的气魄，就不会成为美丽春天里的一枝独秀；荷花如果没有出淤泥而不染的意志，就不会成为炎炎夏日里的一位君子；梅花如果没有傲立霜雪的勇气，就不会成为残酷冬日里的一道靓丽风景；人如果没有坚持到底的毅力，就不会成为紧张军训中的一颗亮星。</p>
            <p>军训不但培养人有吃苦耐劳的精神，而且能磨练人的坚强意志。苏轼有句话：“古之立大事者，不惟有超世之才，亦必有坚忍不拔之志。”这句话意思是成功的大门从来都是向意志坚强的人敞开的，甚至可以说是只向意志坚强的人敞开。</p>
            <p>心目中的军训充满教官的训斥；心目中的军训是紧张与艰苦的合奏；心目中的军训更是无常地响起那集合手哨音，还有那瓢泼大雨下的大集合、严训练；心目中的军训生活成百上千次地浮现在我的脑海，闪现在我的眼前。但我还是满怀欣喜与信心地去拥抱我向往已久的军训。</p>
            <p>在军训中，很苦很累，但这是一种人生体验，战胜自我，锻炼意志的最佳良机。心里虽有说不出的酸甜苦辣，在烈日酷暑下的曝晒，皮肤变成黑黝黝的，但这何尝不是一种快乐，一种更好地朝人生目标前进的勇气，更增添了一份完善自我的信心吗？</p>
            <p>我满怀信心地开始了真正的训练。教官一遍遍耐心地指导代替了严厉的训斥。训练场上不但有我们整齐的步伐，也有我们阵阵洪亮的口号声，更有我们那嘹亮的军营歌曲。</p>

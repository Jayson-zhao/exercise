            <h1>《变形金刚》发烧友的看图说话</h1>
            <p>几条故事线都略浅，最后中国国防部长出动的战斗机似乎也没上什么用场。但是，这片子就是要看无脑爆炸，高潮失禁！从头爽到尾，就是high，柱子屌炸，钢索屌炸，特效屌炸，植入屌炸，中国场景也屌炸，IMAX3D屌炸！那些装B黑喷子可以闭嘴了，哗众取宠这个词完美诠释你们！</p>
            <p>电影一开始就上演了恐龙世界让金属化炸弹（seed)轰炸了一遍,即将有机体转化为金属的炸弹直接导致恐龙这种物种GG，这里出现了第一个问题，貌似外星人的炸弹只能把生物体转化为金属体，但是其生命也会同时被剥夺，参见恐龙灭绝，有人说机械恐龙部队，我后面再说。而且后来的解释是外星人通过这种方式收集金属，问题出现了，外星人有这种逆天的科技为什么不从矿石里直接提炼金属，需要把有机体转化，再收集金属，多此一举啊，还杀了那么多无辜生命。而且出现了外星人创世这一概念，即变形金刚也是他们创造的（最近好像很流行生命出现绝非偶然，而来自于人为创造这一概念，如《普罗米修斯》中的工程师），这里开始扇了电影系列自己的脸，既然是人为创造，那变形金刚一的魔方怎么说，不是说是魔方创造了变形金刚么，唯一可能的解释是变形金刚躯体来自于造物主，灵魂由魔方赋予，魔方也是造物主创造。</p>
            <p>动画里面关于造物主也有描写，即赛博坦星球的变身形态，元始天尊，创造了变形金刚，后来五面怪什么的高压统治，激发了变形金刚变形能力，后来后来，威震天为了推翻五面怪的恐怖统治，建立了霸天虎，对你没有看错，威震天并非绝对的邪恶，这个后面再说，总之，创世概念电影动画都有。不过电影我后来就看不懂了，造物主后来打算销毁变形金刚？然后派出变形金刚去消灭变形金刚？这一段原因什么的电影交代的很是不清楚，只是一上来就有个脸上能变出大炮的家伙（姑且称之为赏金猎人吧）在追杀汽车人了。然后Balabala讲了一大堆，你是被创造出来的种种。不过貌似造物主貌似都有喜欢消灭自己创造出来的生命的尿性，结果大部分都是强X不成反被X，比如黑客帝国里人类试图消灭机器人，普罗米修斯里工程师试图消灭人类，最后都落个灭族的下场。</p>

            <h1>《变形金刚》亲历简报</h1>
            <p>专注于“孩之宝”这个词的特许经营权带着一种迫在眉睫的恐惧感重新出现在地图上，迈克尔贝最近对观众注意力的攻击可能是他职业生涯中最轻浮和空洞的工作。变形金刚：最后的骑士肯定是海湾近期电影的一个退步，这些电影展现了视觉野心的爆发，有时还提升了自我意识。即使是先前的变形金刚电影显示出更多的想象力，如果没有其他原因，海湾的3D第一部作品发现他减少了他的散射编辑，以避免让特许小吃投入垃圾桶。3D投影的改进减少了他的商标美学本身就令人作呕的效果，但是随着科幻小说经常教导我们，我们的技术进步将毁灭我们所有人，并且The Last Knight在完全不可理解的领域中舒适地找回了Bay。</p>
            <p>海湾的动作感一如既往地延伸到剧情中，通过在亚瑟王传奇时期想象变形金刚，这继续让特许经营者无法理解地潜入历史修正主义。在一个令人难以忍受的开场经文中，亚瑟（利亚姆加里根）在梅林（斯坦利·图奇）的帮助下赢得了一场激战，这是一个混乱，醉酒的欺诈者，他不仅发现了一艘坠毁的外星飞船，而且还赢得了变形金刚船员的信任，传说只会从那里扩展。</p>
            <p>在影片的后期，我们发现Autobot Bumblebee（由Erik Aadahl配音），在原版电影中首次登陆地球，实际上在这里居住的时间更长，甚至在第二次世界大战中进行过战斗。如果变形金刚电影更新AP欧洲历史课的想法听起来有点折磨，那么与那些陷入低俗特征的错综复杂的叙事弧相比，这一点都没有。这里的MacGuffin是早期汽车人之一给予Merlin的工作人员 - 这可以消耗地球的生命力并恢复塞伯坦。</p>
            <p>名不见经传的凯德耶格尔（马克华尔伯格）再次陷入变形金刚剧中，被赋予机器人护身符，标志着他是工作人员的捍卫者。并且帮助他的是伊莎贝拉（Isabela Moner），一个斗志旺盛的年轻孤儿，像机械师一样行事和谈话，还有Viviane（Laura Haddock），一位多博士的牛津教授和马球冠军，是梅林的最后一个后裔。</p>
            <p>在整个过程中，Wahlberg可靠地（并且懒洋洋地）以夸张的“你能相信这个吗？”语调来提供剧本的大量博览会。然而，唯一一位从这场混乱中逃脱的演员是安东尼霍普金斯，他将这个离心仪表作为共济会贵族埃德蒙伯顿的曲折。除了在镜头中津津有味地拿着他的薪水之外，这位演员消耗的风景比在“最后的骑士 ”高潮期间摧毁地球的行星杀戮的庞然大物消耗更多的风景，带来可爱的轻浮的可笑背景故事。</p>
